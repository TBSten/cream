context(options: CreamOptions, logger: KSPLogger)
internal fun Appendable.appendCombineToClassFunction(
    primarySource: KSClassDeclaration,
    otherSources: List<KSClassDeclaration>,
    targetClass: KSClassDeclaration,
    generateSourceAnnotation: GenerateSourceAnnotation,
    omitPackages: List<String>,
) {
    val allSources = listOf(primarySource) + otherSources

    targetClass.getConstructors().forEach { constructor ->
        val typeParameters =
            getCopyFunctionTypeParameters(
                sourceClass = primarySource,
                targetConstructor = constructor,
            )

        // Generate copy function
        val funName =
            resolveFunName(
                funNameTemplate = generateSourceAnnotation.funNameTemplate,
                source = primarySource,
                target = targetClass,
                options = options,
            )

        // A ctor param is REQUIRED when it does NOT receive a `= ...` default in the emitted
        // signature: no source supplies its value (neither a type-compatible match nor a
        // value-class wrap/unwrap), or it is @Exclude-marked in any source (`.Exclude` wins over
        // both default kinds). This mirrors the matched/converted/excluded logic in the parameter
        // loop below.
        val requiredCtorParamNames =
            constructor.parameters
                .filter { parameter ->
                    val matched =
                        allSources.any { source ->
                            parameter.findMatchedProperty(source, generateSourceAnnotation) != null
                        }
                    if (matched) {
                        allSources.any { source ->
                            parameter
                                .findMatchedProperty(source, generateSourceAnnotation)
                                ?.let { property -> parameter.isExcludedFromCopy(property, source, generateSourceAnnotation) }
                                ?: false
                        }
                    } else {
                        val conversions =
                            allSources.mapNotNull { source ->
                                parameter
                                    .findValueClassConversion(source, generateSourceAnnotation)
                                    ?.let { conversion -> source to conversion }
                            }
                        conversions.isEmpty() ||
                            conversions.any { (source, conversion) ->
                                parameter.isExcludedFromCopy(conversion.sourceProperty, source, generateSourceAnnotation)
                            }
                    }
                }.map { it.name!!.asString() }

        appendCombineToClassKDoc(allSources, targetClass, generateSourceAnnotation, funName, requiredCtorParamNames)

        deprecatedAnnotationLine(allSources)?.let { appendLine(it) }

        append("${generateSourceAnnotation.visibility.toModifierString(targetClass)} fun ")
        appendTypeParameterList(typeParameters, omitPackages)
        append(" ")
        appendSourceClassWithTypeArgs(primarySource, targetClass, typeParameters)
        append(".")
        append("$funName(")
        appendLine()

        // Add parameters for other source classes
        otherSources.forEach { otherSource ->
            val paramName = otherSource.underPackageName.replaceFirstChar { it.lowercase() }
            append("    $paramName: ${otherSource.fullName}")

            // Add type parameters if the source class has them
            // Map type parameter names to ensure consistency across all sources and target
            if (otherSource.typeParameters.isNotEmpty()) {
                append("<")
                append(
                    otherSource.typeParameters.joinToString(", ") { typeParam ->
                        // Find the mapped type parameter name in the typeParameters map
                        // This ensures all sources and target use the same type parameter names
                        typeParameters.entries
                            .find { (_, param) ->
                                param.source?.name?.asString() == typeParam.name.asString() ||
                                    param.target?.name?.asString() == typeParam.name.asString()
                            }?.key ?: typeParam.name.asString()
                    },
                )
                append(">")
            }

            append(",")
            appendLine()
        }

        // The same renderer produces the parameter type AND any value-class constructor call in
        // its default, so the two can never disagree on omitted packages / type parameters.
        val renderType: (KSType) -> String = { type ->
            type.asString(
                omitPackages = omitPackages,
                typeParameterToString = {
                    resolveConstructorParamTypeParameter(
                        it.declaration as KSTypeParameter,
                        primarySource,
                        targetClass,
                        typeParameters,
                    )
                },
            )
        }

        // Add parameters for constructor
        constructor.parameters.forEach { parameter ->
            val paramName = parameter.name!!.asString()
            val paramType = renderType(parameter.type.resolve())
            val modifiers = if (parameter.isVararg) "vararg " else ""
            append("    $modifiers${paramName.escapeKotlinIdentifier()}: $paramType")

            // Try to find matching property in any of the source classes
            // Use asReversed() to prioritize later source classes when properties overlap
            val matchedSourceAndProperty =
                allSources.asReversed().firstNotNullOfOrNull { source ->
                    parameter
                        .findMatchedProperty(source, generateSourceAnnotation)
                        ?.let { property -> source to property }
                }

            // Suppress the auto-copy default when the matched property is @Exclude-marked in ANY
            // contributing source — not only the "winning" source that supplies the value. With
            // overlapping property names the winner differs per generated function, so checking
            // only the winner would make an explicit @CombineTo.Exclude silently ineffective in
            // some generated functions.
            val excluded =
                allSources.any { source ->
                    parameter
                        .findMatchedProperty(source, generateSourceAnnotation)
                        ?.let { property -> parameter.isExcludedFromCopy(property, source, generateSourceAnnotation) }
                        ?: false
                }

            if (matchedSourceAndProperty != null) {
                val (matchedSource, matchedProperty) = matchedSourceAndProperty
                if (!excluded) {
                    val qualifier = matchedSource.defaultValueQualifier(primarySource)
                    append(" = $qualifier.${matchedProperty.simpleName.asString().escapeKotlinIdentifier()}")
                }
            } else {
                // No type-compatible match in any source: a value-class wrap/unwrap may still
                // supply a default (issue #21). Same asReversed() priority as the matched-property
                // lookup above so later sources win.
                val conversionOutcomes =
                    allSources.asReversed().map { source ->
                        source to parameter.findValueClassConversionOutcome(source, generateSourceAnnotation)
                    }
                val convertedSourceAndConversion =
                    conversionOutcomes.firstNotNullOfOrNull { (source, outcome) ->
                        (outcome as? ValueClassConversionOutcome.Applied)?.let { source to it.conversion }
                    }
                if (convertedSourceAndConversion != null) {
                    val (convertedSource, conversion) = convertedSourceAndConversion
                    // Suppress the conversion default when the conversion-resolved property is
                    // @Exclude-marked in ANY contributing source (`.Exclude` wins), mirroring the
                    // any-source rule for matched properties above.
                    val conversionExcluded =
                        allSources.any { source ->
                            parameter
                                .findValueClassConversion(source, generateSourceAnnotation)
                                ?.let { c -> parameter.isExcludedFromCopy(c.sourceProperty, source, generateSourceAnnotation) }
                                ?: false
                        }
                    if (!conversionExcluded) {
                        val qualifier = convertedSource.defaultValueQualifier(primarySource)
                        val sourceAccess =
                            "$qualifier.${conversion.sourceProperty.simpleName.asString().escapeKotlinIdentifier()}"
                        append(" = ${conversion.defaultValueExpression(sourceAccess, renderType)}")
                    }
                } else {
                    // An almost-conversion (nullability / visibility) is plausibly a user error;
                    // warn once (highest-priority source first) unless the candidate is
                    // `@Exclude`-marked (an explicit opt-out).
                    conversionOutcomes
                        .firstNotNullOfOrNull { (source, outcome) ->
                            (outcome as? ValueClassConversionOutcome.NearMiss)?.let { source to it }
                        }?.let { (source, nearMiss) ->
                            if (!parameter.isExcludedFromCopy(nearMiss.candidate, source, generateSourceAnnotation)) {
                                logger.warn(nearMiss.message, parameter)
                            }
                        }
                }
                parameter.warnIfTargetExcludeHasNoEffect(
                    hasCandidateDefault = convertedSourceAndConversion != null,
                    generateSourceAnnotation = generateSourceAnnotation,
                    logger = logger,
                )
            }

            append(",\n")
        }
        append(") : ")
        appendTargetClassWithTypeArgs(primarySource, targetClass, typeParameters)
        appendWhereClause(typeParameters, omitPackages)
        appendConstructorCallBody(targetClass, constructor)
    }
}

/**
 * The expression that qualifies a default value drawn from [this] source: the receiver (`this`)
 * for the primary source, otherwise this source's lower-camelized simple name — the same
 * convention the matched-property default emission has always used for secondary sources.
 */
private fun KSClassDeclaration.defaultValueQualifier(primarySource: KSClassDeclaration): String =
    if (this == primarySource) "this" else simpleName.asString().replaceFirstChar { it.lowercase() }

private fun Appendable.appendCombineToClassKDoc(
    sources: List<KSClassDeclaration>,
    target: KSClassDeclaration,
    generateSourceAnnotation: GenerateSourceAnnotation,
    funName: String,
    requiredCtorParamNames: List<String>,
) {
    val requiredArgs = requiredCtorParamNames.map { "$it = $it" }
    appendAutoGeneratedFunctionKDoc(
        generateSourceAnnotation = generateSourceAnnotation,
        seeClasses = sources + target,
        autoDescription = {
            appendCombineAutoDescription(sources, target)
        },
        autoExamples = {
            appendExample("Example: Basic", combineExampleBody(sources, funName, extraArgs = requiredArgs))
            appendExample(
                "Example: Override property values",
                combineExampleBody(sources, funName, extraArgs = requiredArgs + "property = value"),
            )
        },
    )
}
