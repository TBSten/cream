package me.tbsten.cream.ksp.core.copyFun

import com.google.devtools.ksp.symbol.KSClassDeclaration
import me.tbsten.cream.ksp.core.common.GenerateSourceAnnotation
import me.tbsten.cream.ksp.core.common.appendAutoGeneratedFunctionKDoc
import me.tbsten.cream.ksp.core.common.appendExample
import me.tbsten.cream.ksp.core.common.fullName
import me.tbsten.cream.ksp.core.common.resolveFunName
import me.tbsten.cream.ksp.core.common.toModifierString
import me.tbsten.cream.ksp.core.common.underPackageName
import me.tbsten.cream.ksp.options.CreamOptions

context(options: CreamOptions)
internal fun Appendable.appendCopyToObjectFunction(
    source: KSClassDeclaration,
    targetObject: KSClassDeclaration,
    generateSourceAnnotation: GenerateSourceAnnotation,
) {
    val funName =
        resolveFunName(
            funNameTemplate = generateSourceAnnotation.funNameTemplate,
            source = source,
            target = targetObject,
            options = options,
        )
    appendCopyToObjectKDoc(source, targetObject, generateSourceAnnotation, funName)
    append("${generateSourceAnnotation.visibility.toModifierString(targetObject)} fun ")
    append(source.fullName)

    if (source.typeParameters.isNotEmpty()) {
        append("<")
        append(
            source.typeParameters
                .joinToString(", ") { "*" },
        )
        append(">")
    }

    append(".$funName() = ${targetObject.fullName}")
    appendLine()
}

private fun Appendable.appendCopyToObjectKDoc(
    source: KSClassDeclaration,
    target: KSClassDeclaration,
    generateSourceAnnotation: GenerateSourceAnnotation,
    funName: String,
) {
    appendAutoGeneratedFunctionKDoc(
        generateSourceAnnotation = generateSourceAnnotation,
        seeClasses = listOf(source, target),
        autoDescription = {
            appendLine("${source.underPackageName} -> ${target.underPackageName} copy function.")
        },
        autoExamples = {
            appendExample(
                "Example: Basic",
                """
                val source = ${source.simpleName.asString()}(...)
                val target = source.$funName()
                """.trimIndent(),
            )
        },
    )
}
